void spi_lcd_clean_line(uint16_t line,uint16_t color)
{
	uint8_t color1,color2;
	TFT_SEND_CMD(0x2a);     //Column address set
	TFT_SEND_DATA(0x00);    //start column
	TFT_SEND_DATA(0x00); 
	TFT_SEND_DATA(0x01);    //end column
	TFT_SEND_DATA(0x40);

	TFT_SEND_CMD(0x2b);     //Row address set
	TFT_SEND_DATA(0x00);    //start row
	TFT_SEND_DATA(line); 
	TFT_SEND_DATA(0x00);    //end row
	TFT_SEND_DATA(line+24);
	TFT_SEND_CMD(0x2C);     //Memory write
	color1 = color>>8;
	color2 = color;
	color = color2<<8|color1;
	SPI_DC_1;
	LCD_CS_0;
	HAL_SPI_Transmit(&hspi1,(uint8_t *)&color, 0, 15360); 
	LCD_CS_1;
}

void spi_lcd_clean_X_Y(uint16_t xs,uint16_t ys,uint16_t xe,uint16_t ye,uint16_t color)
{
	uint32_t  data_length;
	uint8_t color1,color2;
	
	TFT_SEND_CMD(0x2a);     //Column address set
	TFT_SEND_DATA(xs>>8);    //start column
	TFT_SEND_DATA(xs); 
	TFT_SEND_DATA(xe>>8);    //end column
	TFT_SEND_DATA(xe);

	TFT_SEND_CMD(0x2b);     //Row address set
	TFT_SEND_DATA(ys>>8);    //start row
	TFT_SEND_DATA(ys); 
	TFT_SEND_DATA(ye>>8);    //end row
	TFT_SEND_DATA(ye);
	TFT_SEND_CMD(0x2C);     //Memory write
	data_length = (ye-ys+1)*(xe-xs+1)*2;
	color1 = color>>8;
	color2 = color;
	color = color2<<8|color1;
	SPI_DC_1;
	LCD_CS_0;
	while(data_length>0)
	{
		if(data_length>38400)
		{
			HAL_SPI_Transmit(&hspi1,(uint8_t *)&color, 0, 38400); 
			data_length-=38400;
		}
		else{
			HAL_SPI_Transmit(&hspi1,(uint8_t *)&color, 0, data_length); 
			data_length-=data_length;		
		}
	}
	LCD_CS_1;
}
