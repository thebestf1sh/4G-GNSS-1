uint16_t rgb888_to_rgb565(uint8_t red,uint8_t green,uint8_t blue)
{
	uint16_t data;
	uint8_t data1;
	uint8_t data2;
	
	red = (red>>3)&0x1f;
	green = (green>>2)&0x3f;
	blue = (blue>>3)&0x1f;
	data = blue|(green<<5)|(red<<11);
	data1 = data>>8;
	data2 = data;
	data = data2<<8|data1;
	return data;
}

