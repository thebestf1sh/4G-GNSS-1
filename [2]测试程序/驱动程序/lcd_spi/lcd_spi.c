void spi_lcd_draw_bmp(bmp_param *handle,uint8_t *pdata)
{
	uint32_t i,compensation,row,column;
	handle->bfType[0] = *pdata;
	handle->bfType[1] = *pdata+1;
	handle->bfSize = *(pdata+2) |(*(pdata+3)<<8)|(*(pdata+4)<<16)|(*(pdata+5))<<24;
	handle->bfOffset = *(pdata+10) |(*(pdata+11)<<8)|(*(pdata+12)<<16)|(*(pdata+13))<<24;
	handle->biSize = *(pdata+14) |(*(pdata+15)<<8)|(*(pdata+16)<<16)|(*(pdata+17))<<24;
	handle->biWidth = *(pdata+18) |(*(pdata+19)<<8)|(*(pdata+20)<<16)|(*(pdata+21))<<24;
	handle->biHeight = *(pdata+22) |(*(pdata+23)<<8)|(*(pdata+24)<<16)|(*(pdata+25))<<24;
	handle->biBitCount = *(pdata+28) |*(pdata+29)<<8;
	compensation = 4-(handle->biWidth*3)%4;
	if(compensation==4)
	{
		compensation=0;
	}
	row = 0;
	i = 0;
	if(handle->biBitCount == 0x18)
	{
		for(row=0;row<handle->biHeight;row++)
		{
			for(column=0;column<handle->biWidth;column++)
			{
				handle->data[i] = rgb888_to_rgb565(*(pdata+54+2+i*3+compensation*row),*(pdata+54+1+i*3+compensation*row),*(pdata+54+0+i*3+compensation*row));
				i++;
			}
		}
	}
	TFT_SEND_CMD(0x2a); 		//Column address set
	TFT_SEND_DATA(0x00); 		//start column
	TFT_SEND_DATA(0x00); 
	TFT_SEND_DATA(0x00);		//end column
	TFT_SEND_DATA(handle->biWidth);

	TFT_SEND_CMD(0x2b); 		//Row address set
	TFT_SEND_DATA(0x00); 		//start row
	TFT_SEND_DATA(0x00); 
	TFT_SEND_DATA(0x00);		//end row
	TFT_SEND_DATA(handle->biHeight);
	TFT_SEND_CMD(0x2C);			//Memory write

	for(row=handle->biHeight;row>0;row--)
	{
		TFT_SEND_DATA_NOT_SINGLE((uint8_t *)handle->data+2*(handle->biWidth*(row-1)),2*(handle->biWidth));
		TFT_SEND_DATA(0xff);
		TFT_SEND_DATA(0xff);
	}
}

